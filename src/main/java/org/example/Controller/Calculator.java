package org.example.Controller;

public class Calculator {


    public double sum(double a, double b) {
        return a + b;
    }

    public double subtract(double a, double b) {
        return a - b;
    }

    public double div(double a, double b) {
        return a / b;
    }

    public double mul(double a, double b) {
        return a * b;
    }
}
